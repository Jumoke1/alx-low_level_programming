/**
 * get_bit -a funvtion that returns the value of a given index
 * @n: number to extract bit from
 * @index: index
 * Return: the value of
 */

int get_bit(unsigned long int n, unsigned int index)
{
int num;

if (index > 63)
return (-1);

num = (n >> index) & 1;

return (num);
}
